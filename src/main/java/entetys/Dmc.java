package entetys;

public class Dmc {
    int dmcId;
    String dmcName;
    int foodId;
    int busnisId;

    public Dmc() {
    }

    public int getDmcId() {
        return dmcId;
    }

    public void setDmcId(int dmcId) {
        this.dmcId = dmcId;
    }

    public String getDmcName() {
        return dmcName;
    }

    public void setDmcName(String dmcName) {
        this.dmcName = dmcName;
    }

    public int getFoodId() {
        return foodId;
    }

    public void setFoodId(int foodId) {
        this.foodId = foodId;
    }

    public int getBusnisId() {
        return busnisId;
    }

    public void setBusnisId(int busnisId) {
        this.busnisId = busnisId;
    }
}
